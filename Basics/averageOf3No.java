import java.util.*;
public class averageOf3No {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        float A = sc.nextFloat();
        float B = sc.nextFloat();
        float C = sc.nextFloat();
        float D = (A+B+C)/3;
        System.out.println("The average of 3 no. is " + D);
    }
}
